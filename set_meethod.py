# 4️⃣ pop() – Removes and returns a random element from the set.
# 5️⃣ clear() – Removes all elements from the set.
# 6️⃣ copy() – Returns a shallow copy of the set.
# 7️⃣ union(set2, ...) – Returns a new set with all unique elements from both sets.
