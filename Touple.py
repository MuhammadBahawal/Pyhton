# Operations in touple

# 1. **Creating a Tuple**: Creates a tuple with elements.
# 2. **Accessing Elements**: Access elements using indexing.
# 3. **Slicing a Tuple**: Extract a subtuple using slice notation.
# 4. **Concatenation**: Combine two or more tuples using `+`.
# 5. **Repetition**: Repeat the tuple elements using `*`.
# 6. **Length of a Tuple**: Get the number of elements in the tuple using `len()`.
# 7. **Checking for Existence**: Check if an element exists in the tuple using `in`.
# 8. **Tuple Unpacking**: Assign tuple elements to individual variables.
# 9. **Nested Tuples**: Tuples can contain other tuples.
# 11. **count()**: Count occurrences of an element in the tuple.
# 12. **index()**: Find the index of the first occurrence of an element.

