# methods that we use in for list

# 1.  append() Adds a single element to the end of the list.
# 2.  extend()  Adds all elements from an iterable (e.g., another list) to the end of the list.
# 3.  insert()  Inserts an element at a specified position (index) in the list.
# 4.  remove()  Removes the first occurrence of a specified element from the list.
# 5.  pop()  Removes and returns an element at a specified index (or the last element if no index is specified).
# 6.  clear()  Removes all elements from the list, making it empty.
# 7.  index()  Returns the index of the first occurrence of a specified element.
# 8.  count()  Returns the number of times a specified element appears in the list.
# 9.  sort()  Sorts the elements of the list in ascending order (by default).
# 10. reverse()  Reverses the elements of the list in place.
# 11. copy()  Returns a shallow copy of the list (a new list with the same elements).
